    public void loadGioHang(GioHang gh) {
        txtMaKhachHang.setText(gh.getMaKhachHang());
        txaSanPham.setText("");
        for (SanPham sp : gh.getListSanPham()) {
            String Masanpham = sp.getMaSanPham();
            String Tensanpham = sp.getTenSanPham();
            int Soluong = sp.getSoluong();
            String giatien = UtilityFormat.DinhdangVnd(sp.getGiatien());
            txaSanPham.append("Tên sản phẩm : " + Tensanpham + "\n"
                    + "Mã sản phẩm : " + Masanpham + "\n"
                    + "Số lượng : " + Soluong + "\n"
                    + "Giá tiền/1 Sản phẩm: " + giatien + "\n\n"
            );
        }
        txtTongTien.setText(UtilityFormat.DinhdangVnd(gh.tongtien()));
    }

    public boolean CreateHoaDon(String MaHoaDon) {
        if (MaHoaDon != null && !MaHoaDon.isBlank()) {
            HoaDon hd = new HoaDon();
            hd.setMaHoaDon(MaHoaDon);
            hd.setMaKhachHang(giohang.getMaKhachHang());
            hd.setMaNhanVien(User.getUser().getMaNhanVien());

            int Error = new HoaDonDAO().Them(hd);
            switch (Error) {
                case 2627 -> {
                    JOptionPane.showMessageDialog(txtTongTien, "Mã đơn hàng đã tồn tại");
                    return false;
                }
                case 51000 -> {
                    JOptionPane.showMessageDialog(txtTongTien, "Mã khách hàng không tồn tại");
                    return false;
                }
                default -> {
                    return true;
                }
            }
        } else {
            return false;
        }
    }

    public boolean CreateHoaDonChiTiet(String MaHoaDon) {
        int Error = 0;
        for (SanPham sp : giohang.getListSanPham()) {
            HoaDonChiTiet hdct = new HoaDonChiTiet();
            hdct.setMaHoaDon(MaHoaDon);
            hdct.setLoaiSanpham(sp.getLoaiSanPham());
            hdct.setMaSanPham(sp.getMaSanPham());
            hdct.setSoluong(sp.getSoluong());
            hdct.setGiaTien(sp.getGiatien());
            Error = new HoaDonChiTietDAO().Them(hdct);
            switch (Error) {
                case 51000 -> {
                    JOptionPane.showMessageDialog(txtTongTien, "Mã xe không tồn tại");
                    return false;
                }
                case 51001 -> {
                    JOptionPane.showMessageDialog(txtTongTien, "Mã phụ kiện không tồn tại");
                    return false;
                }
                case 51002 -> {
                    JOptionPane.showMessageDialog(txtTongTien, "Số lượng không hợp lý");
                    return false;
                }
                case 52000 -> {
                    JOptionPane.showMessageDialog(txtTongTien, "Mã sản phẩm không hợp lệ");
                    return false;
                }
                default -> {

                }
            }
        }
        return true;
    }

    public String autoId() {
        int nextId = new HoaDonDAO().getLastId() + 1;
        return "HD" + nextId;
    }

