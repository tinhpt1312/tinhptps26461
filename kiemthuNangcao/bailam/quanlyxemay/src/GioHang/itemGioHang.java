    private void btnXoasanphamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoasanphamActionPerformed
        String mangsanpham = JOptionPane.showInputDialog("Nhập mã sản phẩm ");
        if (mangsanpham != null && !mangsanpham.isBlank()) {
            boolean check = ControllerGioHang.XoaNhieuSanPhamChoKhach(txtMaKhachHang.getText(), mangsanpham);
            if (check) {
                main.LoadTable();
                JOptionPane.showMessageDialog(txtTongTien, "Xóa sản phẩm thành công");
            } else {
                JOptionPane.showMessageDialog(txtTongTien, "Mã sản phẩm không đúng");
            }
        }
    }//GEN-LAST:event_btnXoasanphamActionPerformed

    private void btnThanhtoanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhtoanActionPerformed
        if (!giohang.getListSanPham().isEmpty()) {
            if (Utility.UtilityNotificate.confirmDialog("Xác nhận thanh toán") == 1) {
                String MaHoaDon = autoId();
                boolean result = false;
                if (MaHoaDon != null && !MaHoaDon.isBlank()) {
                    result = CreateHoaDon(MaHoaDon);
                    if (result) {
                        result = CreateHoaDonChiTiet(MaHoaDon);
                        if (!result) {
                            JOptionPane.showMessageDialog(txtTongTien, "Thanh toán thất bại");
                        } else {
                            ControllerGioHang.XoaGioHang(giohang.getMaKhachHang());
                            main.LoadTable();
                            JOptionPane.showMessageDialog(txtTongTien, "Thanh toán thành công");
                        }
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(txtTongTien, "Giỏ hàng rỗng");
        }
    }//GEN-LAST:event_btnThanhtoanActionPerformed


