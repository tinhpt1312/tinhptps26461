    private void btnTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTestActionPerformed
        // TODO add your handling code here:
        String fullName = txtHoten.getText();
        if (fullName.length() == 0) {
            JOptionPane.showMessageDialog(null, "Không để trống");
            txtHoten.setBackground(Color.yellow);
            txtHoten.requestFocus();
        } else {
            txtHoten.setBackground(Color.white);
            if (txtLuong.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Không để trống");
                txtLuong.setBackground(Color.yellow);
                txtLuong.requestFocus();
            } else {
                try {
                    Double Luong = Double.parseDouble(txtLuong.getText());
                    if (Luong < 0) {
                        JOptionPane.showMessageDialog(null, "Lương phải là số dương");
                        txtLuong.setBackground(Color.yellow);
                        txtLuong.requestFocus();
                    }else{
                        txtLuong.setBackground(Color.white);
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Không đúng định dạng");
                    txtLuong.setBackground(Color.yellow);
                    txtLuong.requestFocus();
                }
            }
        }
        try {
            XDate.parse(txtNgaysinh.getText());
            txtNgaysinh.setBackground(Color.white);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Chưa đúng định dạng (dd-MM-yyyy)");
            txtNgaysinh.setBackground(Color.YELLOW);
        }
        
        JOptionPane.showMessageDialog(null, "Thành công");
    }//GEN-LAST:event_btnTestActionPerformed

