    private void btnemailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnemailActionPerformed
        // TODO add your handling code here:
        // \\w+ là tối thiểu 1 chữ
        //{1,2} có thể có 1 hay 2 chuỗi như v
        String mauEmail = "\\w+@\\w+(\\.\\w+){1,2}";
        /* lấy Email nhập vào */
        String s = txtemail.getText();
        /* kiểm tra email nhập vào có đúng mẫu hay k */
        if(s.matches(mauEmail)){
            JOptionPane.showMessageDialog(null,"Email đúng");
        }else  JOptionPane.showMessageDialog(null,"Email không đúng định dạng");
    }//GEN-LAST:event_btnemailActionPerformed

