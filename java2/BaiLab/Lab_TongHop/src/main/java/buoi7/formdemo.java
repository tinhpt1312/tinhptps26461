    private void btnTuoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTuoiActionPerformed
        // TODO add your handling code here:
        String s = txtNamsinh.getText();
        if (s.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Chua co du lieu");
        } else {
            try {
                int namSinh = Integer.parseInt(s);
                if (namSinh < 0) {
                    JOptionPane.showMessageDialog(null, "Nam sinh phai > 0");
                } else {
                    JOptionPane.showMessageDialog(null, "Tuoi = " + (2023 - namSinh));
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Nam sinh phai la so");
            }

        }
    }//GEN-LAST:event_btnTuoiActionPerformed

