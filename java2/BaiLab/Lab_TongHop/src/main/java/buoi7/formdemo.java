    private void btnTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTestActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sf = new SimpleDateFormat();
        try {
            /* Qui định dạng ngày tháng nhập/ xuất */
            sf.applyPattern("dd-MM-yy");
            /* Lấy chuỗi ngày nhập vào */
            String ngay = txtNgay.getText();
            /* Kiểm tra có đúng mẫu */
            Date d = sf.parse(ngay);
            /* Xuất ra ngày nhập vào theo dạng khác */
            sf.applyPattern("MMM, dd-yyyy");
            ngay = sf.format(d); /* Chuyển ngày tháng sang dạng chuỗi */
            JOptionPane.showMessageDialog(null, ngay);
        } catch (ParseException ex) {
            /* Nếu sai mẫu */
            JOptionPane.showMessageDialog(null, "Sai định dạng ngày");
        }
    }//GEN-LAST:event_btnTestActionPerformed

