    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String path = txtFile.getText();
        String noidung = txtNoiDung.getText();
        try{
            BufferedWriter bw = new BufferedWriter(new FileWriter(path));
            bw.write(noidung);
            bw.newLine();
            bw.close();
            JOptionPane.showMessageDialog(null, "Ghi file thành công");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Lỗi ghi file");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocActionPerformed
       String path = txtFile.getText();
       try{
           BufferedReader br = new BufferedReader(new FileReader(path));
           while(true){
               String line = br.readLine();
               if(line == null){
                   break;
               }else{
                   txtNoiDung.append(line);
                   txtNoiDung.append("\r\n");
               }
               
           }
           br.close();
           JOptionPane.showMessageDialog(null, "Đọc file thành công");
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Lỗi đọc file");
       }
    }//GEN-LAST:event_btnDocActionPerformed

