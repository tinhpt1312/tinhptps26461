    private void addStaff(){
        Staff nv = new Staff();
        nv.fullname = txtHoten.getText();
        nv.salary = Double.parseDouble(txtLuong.getText());
        lStaff.add(nv);
        fillToTable();
    }
    
   private void fillToTable(){
       DefaultTableModel model = (DefaultTableModel)lblDanhsach.getModel();
       model.setRowCount(0);
       for(Staff nv: lStaff){
           model.addRow(new Object[]{
               nv.getFullname(),
               nv.getSalary()
           });
       }
   }
