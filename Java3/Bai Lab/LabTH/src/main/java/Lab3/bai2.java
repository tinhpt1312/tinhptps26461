    private void btnokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnokActionPerformed
        // TODO add your handling code here:
        String str = "";
        if(check()){
            str = "Họ tên: " +txtho.getText() +" "+ txtten.getText()+ "\n";
            str = str + "Quê quán: " +cboque.getSelectedItem() + "\n";
            if(rdonam.isSelected()){
                str = str + "Giới tính: Nam";
            }if(rdonu.isSelected()){
                str = str + "Giới tính: Nữ";
            }if(rdonone.isSelected()){
                str = str + "Giới tính: Không xác định";
            }
        }
        JOptionPane.showMessageDialog(null, str,"Thông tin cá nhân",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnokActionPerformed

