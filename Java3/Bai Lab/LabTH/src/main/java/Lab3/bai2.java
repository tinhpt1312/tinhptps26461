    public boolean check() {
        if (txtho.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập họ");
            txtho.requestFocus();
            return false;
        }
        if (txtten.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Chưa nhập tên");
            txtten.requestFocus();
            return false;
        }
        return true;
    }
