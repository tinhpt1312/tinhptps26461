    public void sendEmail() {
        // Mật khẩu: nqokqttfzhnwqkbb
        Properties prop = new Properties();
        prop.put("mail.smtp.host", "smtp.gmail.com");
        prop.put("mail.smtp.port", "587");
        prop.put("mail.smtp.auth", "true");
        prop.put("mail.smtp.starttls.enable", "true"); //TLS
        Session session = Session.getInstance(prop,
                new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(txtMail.getText(), new String(txtPass.getPassword()));
            }
        });
        try {
            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress("phamthetinhcop1312@gmail.com"));
            message.setRecipients(
                    Message.RecipientType.TO,
                    InternetAddress.parse(txtMailnhan.getText())
            );
            if (!txtCC.getText().isEmpty()) {
                message.setRecipients(Message.RecipientType.CC, InternetAddress.parse(txtCC.getText()));
            }
            message.setSubject(txtSubject.getText());
            message.setText(txtMessage.getText());
            if (fileAttachment != null) {
                MimeBodyPart messageBodyPart = new MimeBodyPart();
                DataSource source = new FileDataSource(fileAttachment);
                messageBodyPart.setDataHandler(new DataHandler(source));
                messageBodyPart.setFileName(fileAttachment.getName());

                Multipart multipart = new MimeMultipart();
                multipart.addBodyPart(messageBodyPart);

                message.setContent(multipart);
            }
            Transport.send(message);
            JOptionPane.showMessageDialog(null, "Gửi thành công");
        } catch (MessagingException e) {
            e.printStackTrace();
        }
    }

