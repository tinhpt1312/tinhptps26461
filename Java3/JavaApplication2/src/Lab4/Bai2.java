    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       Vector dataRow = new Vector();
       dataRow.add(txtMasp.getText().trim());
       dataRow.add(txtTensp.getText().trim());
       dataRow.add(cboDVT.getSelectedItem());
       dataRow.add(txtDongia.getText().trim());
       dataRow.add(txtNhacc.getText().trim());
       modelProduct.addRow(dataRow);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tblProduct.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null,"Chưa chọn sản phẩm nào trong bảng");
            return;
        }
        modelProduct.removeRow(row);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = tblProduct.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null,"Chưa chọn sản phẩm nào trong bảng. \nThao tác thất bại");
            return;
        }
        modelProduct.setValueAt(txtTensp.getText().trim(),row,1);
        modelProduct.setValueAt(cboDVT.getSelectedItem(),row,2);
        modelProduct.setValueAt(txtDongia.getText().trim(),row,3);
        modelProduct.setValueAt(txtNhacc.getText().trim(),row,4);
    }//GEN-LAST:event_jButton3ActionPerformed

